"""
Corpus loader for the one-month e2e cost benchmark.

The body banks were generated by the v3 benchmark pipeline at commit
``535dab2`` and restored here into ``benchmark/data/corpus/`` as two
flat JSON lists of ``{"headline": str, "body": str}`` entries:

- ``digest_bodies.json`` -- 2316 entries, EU energy and climate policy
  (matches s01 of the v3 scenario pack).
- ``event_bodies.json`` -- 1218 entries, rare-earth supply-chain news
  (matches s03).

``build_timeline`` rotates through the chosen bank so a cohort of
``len(source_urls) * items_per_source_per_day`` items sees a fresh
headline each slot before any repeats, and emission timestamps are
evenly spaced inside each simulated day.

The user specs and retrieval queries mirror the two s01 / s03 personas
closely enough for the real Conversational Agent to author something
very similar during onboarding, but the test overwrites the sub rows
with these pinned strings so downstream quality does not drift with
model changes.
"""

from __future__ import annotations

import json
from datetime import UTC, datetime, timedelta
from pathlib import Path

from news_benchmark.fakes.adapters import ScenarioItem

_CORPUS_DIR = Path(__file__).resolve().parents[2] / "data" / "corpus"


DIGEST_USER_SPEC = (
    "# EU energy and climate policy daily digest\n"
    "\n"
    "Give me a daily round-up of EU energy and climate policy: Council "
    "decisions, Commission proposals, ENTSO-E / ACER publications, "
    "EUR-Lex directives, Parliament committee votes (ENVI, ITRE). "
    "Focus on policy, not market prices.\n"
    "\n"
    "Skip: EV sales news, sports, celebrity coverage, generic tech."
)

DIGEST_RETRIEVAL_QUERY = (
    "EU energy and climate policy Council Commission ENTSO-E ACER "
    "EUR-Lex ENVI ITRE methane renewable wind LNG gas storage"
)


EVENT_USER_SPEC = (
    "# Rare-earth supply-chain event alerts\n"
    "\n"
    "Notify me the instant a named rare-earth supply-chain event "
    "breaks: export bans or quotas (dysprosium, neodymium, terbium, "
    "praseodymium), sanctions, mine or refinery closures, force-"
    "majeure declarations, stockpile and offtake announcements.\n"
    "\n"
    "Do NOT notify on: routine price moves, general analysis pieces, "
    "opinion, other commodities (lithium, cobalt, copper, nickel) "
    "unless the story is directly rare-earth-linked."
)

EVENT_RETRIEVAL_QUERY = (
    "rare earth supply chain dysprosium neodymium terbium praseodymium "
    "export ban quota mine refinery closure force majeure"
)


def _load_bank(name: str) -> list[tuple[str, str]]:
    """Read the restored body bank from disk as ``[(headline, body), ...]``."""
    path = _CORPUS_DIR / name
    raw = json.loads(path.read_text())
    return [(entry["headline"], entry["body"]) for entry in raw]


def _bank_for(topic: str) -> list[tuple[str, str]]:
    if topic == "digest":
        return _load_bank("digest_bodies.json")
    if topic == "event":
        return _load_bank("event_bodies.json")
    raise ValueError(f"unknown topic: {topic!r}")


def build_timeline(
    *,
    source_urls: list[str],
    topic: str,
    start: datetime,
    days: int,
    items_per_source_per_day: int,
) -> list[ScenarioItem]:
    """Spread ``items_per_source_per_day`` items across ``days`` per source.

    Items are taken from the bank matching ``topic`` (``"digest"`` or
    ``"event"``), rotated so every item appears before repeats start.
    Emission timestamps are evenly spaced inside each day so the
    pipeline observes a steady stream rather than bursts.
    """
    if start.tzinfo is None:
        raise ValueError("start must be timezone-aware")

    bank = _bank_for(topic)
    items: list[ScenarioItem] = []
    counter = 0
    for day in range(days):
        for slot in range(items_per_source_per_day):
            hour_offset = int((slot + 0.5) * (24 / max(items_per_source_per_day, 1)))
            ts = start + timedelta(days=day, hours=hour_offset)
            for source_url in source_urls:
                headline, body = bank[counter % len(bank)]
                items.append(
                    ScenarioItem(
                        fake_ts=ts.astimezone(UTC),
                        source_url=source_url,
                        headline=headline,
                        body=body,
                    )
                )
                counter += 1
    return items
